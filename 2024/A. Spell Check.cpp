#include<bits/stdc++.h>
using namespace std;
void solve()
{
    int s;
    cin>>s;
    string st[s];
    cin>>st[s];

    for(int i=0;i<s;i++)
    {
       if(st[i] == "T")
        cout<<"YES"<<endl;
       else
        cout<<"NO"<<endl;
        continue;
    }



}
int main()
{
    int t;
    cin>>t;
    while(t--)
    {
        solve();

    }
    return 0;
}

